<p align="justify">
	<b>NextCloud</b> é mais conhecido como "serviço de armazenamento e sincronização de arquivos". 
	Como tal, é muito semelhante a outras núvens amplamente usadas (como: Dropbox, Google Drive, Onedrive, e etc...)
	cuja principal diferença é que o NextCloud é gratuito e tem seu código fonte aberto, e permitindo assim qualquer 
	pessoa de instalar e operar sem custo em um servidor privado, sem limite de espaço de armazenamento (com exceção 
	da capacidade do disco rígido).
</p><br/>

<p align="justify">
	A <a href="?sub=contato">TUATEC</a> está combinando a tecnologia do NextCloud com <a href="?sub=hint_embarcado">Computadores 
	Embarcados de baixo custo</a> para oferecer aos clientes a melhor forma custo benefício de armazenamento remoto 
	seguro e intuitivo. Todos os tipos de arquivos (Documento, Imagens, Áudio, Vídeo, Softaware) pode sem 
	armazenados e compartilhados controladamente entre os usuários cadastrados. Também é possível publicar de 
	downloads de forma organizada.
</p><br/>

