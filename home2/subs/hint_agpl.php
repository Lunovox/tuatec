</p><br/>

<p align="justify">
	<h2>Definição:</h2>
	A Affero General Public License (Licença Pública Geral Affero GNU), ou simplesmente GNU Affero GPL, 
	é uma licença de software livre publicada recentemente pela Free Software Foundation. A GNU AGPL tem 
	o propósito de ser uma licença minimamente modificada da GNU GPL e atender as necessidades de fornecer 
	liberdade em softwares como serviços (SaaS, Software as a Service), ou seja, aqueles aos quais não se 
	tem acesso direto ao binário/código-objeto.
</p><br/>

<p align="justify">
	<h2>As Quarto Liberdades Básicas:</h2>
	Em termos gerais, a GNU AGPL (como também a GNU GPL), baseia-se em 4 liberdades:<br/>
	<br/>
	<ul style="margin-left:30px;">
		<li><b>Liberdade nº0</b> - A liberdade de executar o programa, para qualquer propósito.</li>
		<li><b>Liberdade nº1</b> - A liberdade de estudar como o programa funciona e adaptá-lo para as suas 
		necessidades. O acesso ao código-fonte é um pré-requisito para esta liberdade.</li>
		<li><b>Liberdade nº2</b> - A liberdade de redistribuir cópias de modo que você possa ajudar ao seu próximo.</li>
		<li><b>Liberdade nº3</b> - A liberdade de aperfeiçoar o programa, e liberar os seus aperfeiçoamentos, de modo 
		que toda a comunidade se beneficie deles. O acesso ao código-fonte é um pré-requisito para esta liberdade.</li>
	</ul>
</p><br/>

<p align="justify">
	<h2>Diferença GNU GPL e GNU AGPL:</h2>
	A seção adicional da GNU Affero GPL requer que o código-fonte deva estar completamente disponível para qualquer 
	usuário do software coberto pela GNU AGPL, tipicamente uma aplicação web. A Free Software Foundation recomenda 
	essa licença para qualquer software que será executado atrás de um servidor, do mesmo modo como funciona uma 
	aplicação web.
</p><br/>

