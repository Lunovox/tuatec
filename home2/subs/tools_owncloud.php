<p align="justify">
	<b>OwnCloud</b> é um sistema de computador mais conhecido como "serviço de armazenamento e sincronização de 
	arquivos". Como tal, ownCloud é muito semelhante ao amplamente usado Dropbox, cuja principal diferença 
	é que ownCloud é gratuito e open-source, e permitindo assim qualquer um de instalar e operar sem custo 
	em um servidor privado, sem limite de espaço de armazenamento (com exceção da capacidade do disco rígido) 
	ou o número de clientes conectados.
</p><br/>

<p align="justify">
	A Tuatec está combinando a tecnologia do OwnCloud com <a target="_blank" href="?sub=embarcado">computadores 
	embarcados de baixo custo</a> para oferecer aos clientes a melhor forma custo benefício de armazenamento remoto 
	seguro e intuitivo. Todos os tipos de arquivos (Documento, Imagens, Áudio, Vídeo, Softaware) pode sem 
	armazenados e compartilhados controladamente entre os usuários cadastrados. Também é possível publicar de 
	downloads de forma organizada.
</p><br/>

