<h2><b>SCM - Supply Chain Management</b></h2><br/>

<p align="justify">
	A gestão da cadeia logística, (do inglês: Supply Chain Management), também conhecida como gestão da cadeia de 
	suprimentos no Brasil, gestão da cadeia de fornecimento em Portugal, pipeline logístico ou rede logística, consiste 
	em todas as partes relacionadas seja direta ou indiretamente, na execução do pedido de um cliente. Ela inclui não 
	apenas o fornecedor ou o fabricante, mas também as transportadoras, os armazéns, varejistas e os consumidores finais.
</p><br/>

<p align="justify">
	A palavra "Logistica" tem origem da palavra francesa"Logistique" que significa "a arte de planejar".
</p><br/>

<p align="justify">
	Na cadeia logística padrão, as matérias-primas são procuradas e os bens são produzidos em uma ou mais fábricas, 
	transportadas para armazéns como armazenamento intermédio, e depois transportadas para os retalhistas ou clientes. 
	As estratégias utilizadas para obter uma cadeia logística eficaz consideram as interações entre os vários níveis da 
	cadeia logística, de forma a reduzir o custo e melhorar o serviço prestado. A cadeia logística consiste nos fornecedores, 
	centros de fabricação, armazéns e centros de distribuição, assim como matérias-primas, produtos no processo de fabricação, 
	e produtos finais que circulam entre as fábricas.basicamente a logística é a ciência dos detalhes.
</p><br/>

<p align="justify">
	Assim a gestão da cadeia logística consiste numa série de aproximações utilizadas para integrar eficazmente fornecedores, 
	fabricantes e lojas, para que a mercadoria seja produzida e distribuída nas quantidades ideais, na localização certa e no 
	tempo correcto, com o objectivo de satisfazer o nível de serviço e diminuir os custos ao longo do sistema.
</p><br/>

<p align="justify">
	A cadeia logística não é composta apenas de movimentação de produtos físicos entre empresas. Envolve, também, o fluxo de 
	informação e capitais entre as mesmas companhias. A comunicação é um factor chave para a manutenção e gestão da cadeia 
	logística. Os membros da cadeia logística têm de fazer tudo o que estiver ao seu alcance para melhorar as operações da 
	cadeia, pois são essas medidas que permitem reduzir os custos e aumentar as receitas.
</p><br/>