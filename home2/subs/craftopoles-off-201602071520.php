<p align="justify">
	Minetest é um jogo eletrônico tipo sandbox de aventura, sobrevivência e criação livre 
	vagamente baseado em Minecraft, Infiniminer, e outros semelhantes. A diferença é 
	que é absolutamente gratuito e open source. Graças a isto, o utilizador base carrega 
	continuamente novo conteúdo.
</p><br/>

<p align="justify">
	O projeto foi iniciado por Perttu "celeron55" Ahola em outubro de 2010, e atualmente 
	é desenvolvido por um grupo aleatório. O jogo está disponível nativamente para Windows, 
	Android, Linux, Mac OS X e Livre / Open BSD.
</p><br/>

<p align="justify">
	Este jogo oferece a oportunidade ao jogador de usar toda a sua criatividade artística e estrutural. 
	Isso significa que pode partir blocos, montá-los, forjar ferramentas e outros objetos, construir 
	edifícios, e construir qualquer coisa que possa imaginar. Pode fazer tudo isto em dois modos de jogo: 
	Criativo e Sobrevivência. No modo Criativo terá recursos ilimitado de blocos, e no modo Sobrevivência 
	terá de trabalhar no duro para sobreviver enquanto procura e recolhe seus recursos.
</p><br/>

<p align="justify">
	O melhor do tipo sandbox, em todos os sentidos, é que lhe permite jogar online com amigos criando 
	e construindo nos seus próprios servidores. Isto permitir-lhe-á partilhar espaço com mais duma 
	dezena de jogadores; permitindo que todos colaborem na criação de qualquer tipo de edifício.
</p><br/>

<p align="justify">
	O Minetest é a melhor alternativa gratuita ao Minecraft, Infiniminer, e outros semelhantes. 
	Os benefícios e as possibilidades não acabam aí, mas continuam com a sua própria visão. 
	O melhor que pode fazer é dar uma olhada às imagens e ver o potencial ilimitado dentro do Minetest.
</p><br/>

