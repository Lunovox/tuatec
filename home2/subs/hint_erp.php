<h2><b>ERP - Enterprise Resource Planning</b></h2><br/>

<p align="justify">
	Planejamento de recurso corporativo (em inglês Enterprise Resource Planning - ERP) é um sistema de informação 
	que integra todos os dados e processos de uma organização em um único sistema. A integração pode ser vista sob 
	a perspectiva funcional (sistemas de finanças, contabilidade, recursos humanos, fabricação, marketing, vendas, 
	compras etc) e sob a perspectiva sistêmica (sistema de processamento de transações, sistemas de informações 
	gerenciais, sistemas de apoio a decisão, etc).
</p><br/>

<p align="justify">
	O ERP é uma plataforma de software desenvolvida para integrar os diversos departamentos de uma empresa, possibilitando 
	a automação e armazenamento de todas as informações do negócios.<br/>
</p><br/>

<p align="justify">
	ERP é a espinha dorsal dos negócios eletrônicos, uma arquitetura de transações que liga todas as funções de uma empresa, 
	por exemplo, de processamento de pedido de vendas, controle e gerenciamento de estoque, planejamento de produção e 
	distribuição e finanças.
</p><br/>