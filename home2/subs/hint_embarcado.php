	<h2>MINI-COMPUTADOR RASPBERRY PI</h2>
</p><br/>

<p align="justify">
	Raspberry Pi é um computador do tamanho de um cartão de crédito, que se conecta a um monitor de computador 
	ou TV, e usa um teclado e um mouse padrão, desenvolvido no Reino Unido pela Fundação Raspberry Pi. Todo 
	o hardware é integrado numa única placa. 
</p><br/>

<p align="justify">
	O Raspberry Pi é bastante utilizado como um computador embarcado 
	por ter arquitetura <a target="_blank" href="?sub=hint_soc"><b>SoC</b></a> de baixo custo, mas também por 
	conter um poderoso processador capaz de executar diversos tipos de tarefas dedicadas. Embora que o objetivo 
	inicial era promover unicamente o ensino em Ciência da Computação básica em escolas. Hoje ele sendo também 
	para: Hospedar sites, Nuvem de Armazenamento, Central de Aúdio-chat, Vigilância por Câmera, Servidor de 
	Jogos, e etc.
</p><br/>
