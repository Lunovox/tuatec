<p align="center">
	<h1>COMPUTADORES EMBARCADOS</h1>
</p><br/>

<p align="justify">
	Um sistema embarcado (ou sistema embutido, ou sistema embebido) é um sistema microprocessado no qual 
	o computador é completamente encapsulado ou dedicado ao dispositivo ou sistema que ele controla. 
	Diferentemente de computadores de propósito geral, como o computador pessoal, um sistema embarcado 
	realiza um conjunto de tarefas predefinidas, geralmente com requisitos específicos. Já que o sistema 
	é dedicado a tarefas específicas, através de engenharia pode-se otimizar o projeto reduzindo tamanho, 
	recursos computacionais e custo do produto.
</p><br/>

<p align="justify">
	Em geral tais sistemas não podem ter sua funcionalidade alterada durante o uso. Caso queira-se modificar 
	o propósito é necessário reprogramar todo o sistema.
</p><br/>

<p align="justify">
	Sistemas como PDAs são geralmente considerados sistemas embarcados pela natureza de seu hardware, apesar 
	de serem muito mais flexíveis em termos de software. Alguns exemplos de sistemas embarcados são: MP3 
	players, relógios digitais de pulso, semáforos, roteadores (alguns tipos), e etc.
</p><br/>

