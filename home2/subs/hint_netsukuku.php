<h2>Quais são os pontos fortes do projeto?</h2><br/>

<p align="justify" style="margin-bottom:15px">
	<b>Internet independente:</b> A promessa do Netsukuku é fazer seus usuários se livrarem dos provedores de internet. A idéia central por trás Netsekuku é 
	livrar-se de provedores de Internet. Cada máquina dentro da rede WiFi serve como um roteador que redireciona as informações para todos os outros nós na 
	rede.
</p>

<p align="justify" style="margin-bottom:15px">
	<b>Recursos otimizados:</b> O protocolo Netsukuku é construído para lidar com um grande número de computadores, enquanto que requerem uso mínimo da CPU 
	de computador e recursos de memória.
</p>

<p align="justify" style="margin-bottom:15px">
	<b>Privacidade:</b> O sistema é extremamente privativo por não utilizar protocolo HTTP, o que garante anonimato aos usuários.
</p>

<p align="justify" style="margin-bottom:15px">
	<b>Velocidade:</b> A rede sem fio do Netsukuku permite que a troca de arquivos entre as máquinas seja rápida e eficaz, afinal, não existe o intermédio de 
	um servidor centralizado na comunicação dos micros.
</p>

<p align="justify" style="margin-bottom:15px">
	<b>Economia:</b> Qualquer computador suporta o Netsukuku. As necessidades de hardware e software são mínimas.
</p>

<p align="justify" style="margin-bottom:15px">
	<b>Democracia:</b> O código Netsukuku é liberado sob a licença GNU/GPL, ela está aberta e livremente editável e redistribuível por qualquer pessoa que 
	queira construir sobre ela ou corrigir bugs.
</p>

