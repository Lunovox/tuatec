<h1>Netsukuku</h1><br/>

<p align="justify" style="margin-bottom:15px">
	Netsukuku é um sistema projetado pela empresa italiana <i>FreakNet</i> para lidar com grande números de nodes através de uma rede em P2P, 
	que pode ser uma pequena unidade única de informação tal como uma imagem, um vídeo, um link ou ainda um trecho de código CSS, HTML ou PHP) que 
	exige um consumo mínimo de recursos de CPU e memória. 
</p>
	
<p align="justify" style="margin-bottom:15px">
	A rede pode ser usada para construir uma rede mundial distribuída, usando apenas a conectividade WiFi, tolerante a falhas, anônima, e livre de censura, 
	totalmente independente da Internet. Não depende de qualquer tipo de roteador de backbone (esquema de ligações centrais de um sistema mais amplo, 
	tipicamente de elevado desempenho), provedor de internet, permitindo a comunicação direta entre máquinas sem recorrer ao protocolo HTTP, ou qualquer 
	sistema centralizado, embora possa perder em vantagem dos sistemas existentes dessa natureza para aumentar a unidade e a conectividade com a rede 
	Netsukuku. 
</p>

<p align="justify" style="margin-bottom:15px">
	Ele é um tanto parecido com o <a target="_blank" href="http://blog.deepwebbrasil.com/redes-da-deep-web-byzantium-3/">projeto Byzantium</a>.
</p>

